int		ft_iterative_factorial(int nb)
{
	int c;

	c = nb - 1;
	if (nb < 0 || nb > 13)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (c > 0)
	{
		nb = nb * c;
		c--;
	}
	return (nb);
}
