#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*b;

	b = (char*)s;
	i = 0;
	while (b[i])
	{
		if (b[i] == c)
			return (b + i);
		i++;
	}
	if (b[i] == '\0' && b[i] == c)
		return (b + i);
	return (0);
}
