#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;
	char	*s;

	j = 0;
	i = 0;
	s = (char*)s2;
	while (s1[j])
	{
		j++;
	}
	while (s[i])
	{
		s1[j] = s[i];
		i++;
		j++;
	}
	s1[j] = '\0';
	return (s1);
}
